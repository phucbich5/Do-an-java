    private void timkiemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_timkiemActionPerformed
        try {
            // TODO add your handling code here:
            databseConect c = new databseConect();
            Connection conn = c.getConnection();
            String makhoa = txttimkim.getText();
            PreparedStatement ps = conn.prepareStatement("SELECT * FROM `qlkhoa` WHERE" + "`MaKhoa` like N'%" + makhoa + "%'");
            ResultSet rs = ps.executeQuery();
            Object[] obj = new Object[]{"MaKhoa", "TenKhoa", "SoLop"};
            DefaultTableModel tableModel = new DefaultTableModel(obj, 0);
            khoa.setModel(tableModel);
            while (rs.next()) {
                Object[] item = new Object[3];
                item[0] = rs.getString("MaKhoa");
                item[1] = rs.getString("TenKhoa");
                item[2] = rs.getString("SoLop");
                tableModel.addRow(item);
            }
        } catch (Exception ex) {
            System.out.println(ex);
            JOptionPane.showMessageDialog(this, "Không tìm thấy");
        }
    }//GEN-LAST:event_timkiemActionPerformed

