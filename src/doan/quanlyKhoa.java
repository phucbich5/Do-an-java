    public void loadData() {
        try {
            databseConect c = new databseConect();
            Connection conn = c.getConnection();
            int number;
            Vector row, column;
            column = new Vector();
            Statement st = conn.createStatement();
            ResultSet rs = st.executeQuery("Select * from qlkhoa ");
            ResultSetMetaData matadata = rs.getMetaData();
            number = matadata.getColumnCount(); //trả về số cột

            for (int i = 1; i <= number; i++) {
                column.add(matadata.getColumnName(i));
            }
            dtKhoa.setColumnIdentifiers(column);

            while (rs.next()) {
                row = new Vector();
                for (int i = 1; i <= number; i++) {
                    row.addElement(rs.getString(i));
                }
                dtKhoa.addRow(row);
                khoa.setModel(dtKhoa);
            }
            //Khi chọn vào 1 nhân viên nào đó thì nó sẽ hiện thông tin trên text Field
            khoa.getSelectionModel().addListSelectionListener(new ListSelectionListener() {

                @Override
                public void valueChanged(ListSelectionEvent e) {
                    if (khoa.getSelectedRow() >= 0) {
                        txtMaKhoa.setText(khoa.getValueAt(khoa.getSelectedRow(), 0) + "");
                        txtTenKhoa.setText(khoa.getValueAt(khoa.getSelectedRow(), 1) + "");
                        txtSolop.setText(khoa.getValueAt(khoa.getSelectedRow(), 2) + "");
                    }
                }
            });
        } catch (Exception ex) {
            System.out.println(ex.toString());
        }
    }

