    public void loadData() {
        try {
            databseConect c = new databseConect();
            Connection conn = c.getConnection();
            int number;
            Vector row, column;
            column = new Vector();
            Statement st = conn.createStatement();
            ResultSet rs = st.executeQuery("Select * from qllophoc ");
            ResultSetMetaData matadata = rs.getMetaData();
            number = matadata.getColumnCount(); //trả về số cột

            for (int i = 1; i <= number; i++) {
                column.add(matadata.getColumnName(i));
            }
            abc.setColumnIdentifiers(column);

            while (rs.next()) {
                row = new Vector();
                for (int i = 1; i <= number; i++) {
                    row.addElement(rs.getString(i));
                }
                abc.addRow(row);
                lophoc.setModel(abc);
            }
            //Khi chọn vào 1 nhân viên nào đó thì nó sẽ hiện thông tin trên text Field
            lophoc.getSelectionModel().addListSelectionListener(new ListSelectionListener() {

                @Override
                public void valueChanged(ListSelectionEvent e) {
                    if (lophoc.getSelectedRow() >= 0) {
                        malop.setText(lophoc.getValueAt(lophoc.getSelectedRow(), 0) + "");
                        tenlop.setText(lophoc.getValueAt(lophoc.getSelectedRow(), 1) + "");
                        sosinhvien.setText(lophoc.getValueAt(lophoc.getSelectedRow(), 2) + "");
                        tengiaovien.setText(lophoc.getValueAt(lophoc.getSelectedRow(), 3) + "");
                    }
                }
            });
        } catch (Exception ex) {
            System.out.println(ex.toString());
        }
    }

