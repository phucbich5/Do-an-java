    private void timkiemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_timkiemActionPerformed

        try {
            // TODO add your handling code here:
            databseConect c = new databseConect();
            Connection conn = c.getConnection();
            String malop = tim.getText();
            PreparedStatement ps = conn.prepareStatement("SELECT * FROM `qllophoc` WHERE" + "`MaLop` like N'%" + malop + "%'");
            ResultSet rs = ps.executeQuery();
            Object[] obj = new Object[]{"MaLop", "TenLop", "SoHS", "TenGV", "Khoa"};
            DefaultTableModel tableModel = new DefaultTableModel(obj, 0);
            lophoc.setModel(tableModel);
            while (rs.next()) {
                Object[] item = new Object[5];
                item[0] = rs.getString("MaLop");
                item[1] = rs.getString("TenLop");
                item[2] = rs.getString("SoHS");
                item[3] = rs.getString("TenGV");
                item[4] = rs.getString("Khoa");
                tableModel.addRow(item);
            }
        } catch (Exception ex) {
            System.out.println(ex);
            JOptionPane.showMessageDialog(this, "Không tìm thấy");
        }
    }//GEN-LAST:event_timkiemActionPerformed

